/**
 * UwU checks if a number is even!!
 * @param n - the number we are checking~ pwease give us a real number (｡♥‿♥｡)
 * @returns true if n is even, false if it's odd~ yay!!
 */
export const isEven = (n: number): boolean =>
  // ✨ the magic of modulo!! if the remainder is 0, it's even~ (ﾉ◕ヮ◕)ﾉ*:･ﾟ✧
  n % 2 === 0;
